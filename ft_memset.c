#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*placeholder;

	placeholder = (unsigned char *)b;
	while (len > 0)
	{
		*placeholder = (unsigned char)c;
		placeholder++;
		len--;
	}
	return (b);
}
