#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*a1;
	unsigned char	*a2;

	a1 = dst;
	a2 = (unsigned char *)src;
	if (dst < src)
		return (ft_memcpy(dst, src, len));
	if (dst > src)
		while (len--)
			a1[len] = a2[len];
	return (dst);
}
